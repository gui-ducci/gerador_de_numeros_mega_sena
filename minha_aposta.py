import pandas as pd

def numeros_sorteados():
    # Lê a planilha inteira com a função read_excel()
    df = pd.read_excel('numeros_sorteados.xlsx')

    # Seleciona uma linha aleatória da planilha com a função sample()
    linha_aleatoria = df.sample(1)

    # Armazena os valores da linha selecionada em uma lista
    combinacao = linha_aleatoria[0:35].values.tolist()[0]

    # Retorna a combinação selecionada
    return combinacao

# Lê uma combinação aleatória de números sorteados da planilha
combinacao = numeros_sorteados()

# Imprime a combinação selecionada
print(combinacao)

